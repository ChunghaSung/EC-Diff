#!/usr/bin/env python3
"""
extract_facts.py - Python replacement for the LLVM DiffDep pass.

Parses LLVM IR (.ll) files and generates .facts.smt2 files containing
Datalog ground facts for the EC-Diff semantic diffing tool.

This script was generated by Claude Code to replace the original LLVM 3.8
pass-based approach, enabling the tool to work with any modern clang version.

Usage:
    python3 extract_facts.py <input.ll> <pNumber> [statementMatch.txt] [lockMatch.txt]

Arguments:
    input.ll           - LLVM IR file (produced by: clang -emit-llvm -S -c file.c -O0)
    pNumber            - Program number: 1 or 2
    statementMatch.txt - (Optional, P2 only) Statement ID remapping file
    lockMatch.txt      - (Optional, P2 only) Lock ID remapping file
"""

import sys
import re
import os


# ---------------------------------------------------------------------------
# ID generation helpers
# ---------------------------------------------------------------------------

def hex_id(n):
    """Regular instruction ID: #x0000 .. #xefff"""
    return "#x{:04x}".format(n)

def hex_id_lock(n):
    """Lock instruction ID: #xf000 .. #xffff"""
    s = format(n, 'x')
    return "#xf" + "0" * (3 - len(s)) + s


# ---------------------------------------------------------------------------
# IR name normalization
# ---------------------------------------------------------------------------

def normalize_call_target(name):
    """Normalize an IR call target to a canonical function name."""
    if name.startswith('"') and name.endswith('"'):
        name = name[1:-1]
    # macOS mangling: \01_pthread_join -> pthread_join
    if name.startswith('\\01'):
        name = name[3:]
        if name.startswith('_'):
            name = name[1:]
    return name


def extract_global_name(ref):
    """Extract the variable name from an @global reference."""
    if ref and ref.startswith('@'):
        name = ref[1:]
        # Strip quotes if present
        if name.startswith('"') and name.endswith('"'):
            name = name[1:-1]
        return name
    return None


# ---------------------------------------------------------------------------
# Data structures
# ---------------------------------------------------------------------------

class Instruction:
    def __init__(self, text):
        self.text = text.strip()
        self.result_var = None
        m = re.match(r'(%[\w.]+)\s*=', self.text)
        if m:
            self.result_var = m.group(1)

    @property
    def is_terminator(self):
        t = self.text
        return (t.startswith('br ') or t.startswith('ret ') or
                t == 'ret void' or t == 'unreachable' or
                t.startswith('switch ') or t.startswith('indirectbr '))

    @property
    def is_call(self):
        return 'call ' in self.text and '@' in self.text

    @property
    def is_store(self):
        return self.text.startswith('store ')

    @property
    def is_load(self):
        return 'load ' in self.text and '=' in self.text

    def get_call_target_raw(self):
        m = re.search(r'@("(?:[^"\\]|\\.)*"|[\w.]+)\s*\(', self.text)
        return m.group(1) if m else None

    def get_call_target(self):
        raw = self.get_call_target_raw()
        return normalize_call_target(raw) if raw else None

    def get_call_args_text(self):
        """Return the raw argument string from a call instruction."""
        raw_target = self.get_call_target_raw()
        if not raw_target:
            return ""
        # Escape for regex
        escaped = re.escape(raw_target)
        m = re.search(r'@' + escaped + r'\s*\((.+)\)', self.text)
        if not m:
            # Try without the result assignment prefix
            m = re.search(r'@' + escaped + r'\s*\((.+)\)\s*(?:#\d+)?$', self.text)
        return m.group(1) if m else ""

    def get_call_args(self):
        """Split call arguments by top-level commas."""
        args_str = self.get_call_args_text()
        if not args_str:
            return []
        args = []
        depth = 0
        current = ""
        for ch in args_str:
            if ch == '(':
                depth += 1
            elif ch == ')':
                depth -= 1
            elif ch == ',' and depth == 0:
                args.append(current.strip())
                current = ""
                continue
            current += ch
        if current.strip():
            args.append(current.strip())
        return args

    def get_call_arg_value(self, index):
        """Get the value token (last word) of a call argument."""
        args = self.get_call_args()
        if index < len(args):
            tokens = args[index].split()
            return tokens[-1] if tokens else None
        return None

    def get_store_pointer(self):
        """Extract the pointer operand from a store instruction."""
        parts = self.text.split(',')
        if len(parts) >= 2:
            ptr_part = parts[1].strip()
            # Look for @global or %local reference after 'ptr'
            m = re.search(r'(?:ptr\s+(?:noundef\s+)?)?(@"[^"]+"|@[\w.]+|%[\w.]+)', ptr_part)
            if m:
                return m.group(1)
        return None

    def get_load_pointer(self):
        """Extract the pointer operand from a load instruction."""
        parts = self.text.split(',')
        if len(parts) >= 2:
            ptr_part = parts[1].strip()
            m = re.search(r'(?:ptr\s+(?:noundef\s+)?)?(@"[^"]+"|@[\w.]+|%[\w.]+)', ptr_part)
            if m:
                return m.group(1)
        return None

    def get_successor_labels(self):
        """Extract successor basic block labels from a terminator."""
        labels = []
        t = self.text
        if t.startswith('br i1'):
            for m in re.finditer(r'label\s+%([\w.]+)', t):
                labels.append(m.group(1))
        elif t.startswith('br label'):
            m = re.search(r'label\s+%([\w.]+)', t)
            if m:
                labels.append(m.group(1))
        elif t.startswith('switch'):
            for m in re.finditer(r'label\s+%([\w.]+)', t):
                labels.append(m.group(1))
        return labels


class BasicBlock:
    def __init__(self, label):
        self.label = label
        self.instructions = []
        self.succs = []   # BasicBlock refs
        self.preds = []   # BasicBlock refs


class Function:
    def __init__(self, name):
        self.name = name
        self.blocks = []        # in order
        self.block_map = {}     # label -> BasicBlock

    def build_cfg(self):
        for bb in self.blocks:
            if bb.instructions and bb.instructions[-1].is_terminator:
                for sl in bb.instructions[-1].get_successor_labels():
                    if sl in self.block_map:
                        succ = self.block_map[sl]
                        if succ not in bb.succs:
                            bb.succs.append(succ)
                        if bb not in succ.preds:
                            succ.preds.append(bb)


class Module:
    def __init__(self):
        self.functions = []     # Function objects in definition order
        self.func_map = {}


