# Dominator / Post-Dominator tree computation
# ---------------------------------------------------------------------------

def compute_dom_children(blocks, entry):
    """Compute dominator tree children using iterative dataflow."""
    if not blocks:
        return {}

    all_set = set(blocks)
    dom = {}
    for b in blocks:
        dom[b] = frozenset({b}) if b is entry else frozenset(all_set)

    changed = True
    while changed:
        changed = False
        for b in blocks:
            if b is entry or not b.preds:
                continue
            pred_doms = [dom[p] for p in b.preds if p in dom]
            if not pred_doms:
                continue
            new_dom = frozenset(set.intersection(*[set(d) for d in pred_doms]) | {b})
            if new_dom != dom[b]:
                dom[b] = new_dom
                changed = True

    # Immediate dominators -> children map
    children = {b: [] for b in blocks}
    for b in blocks:
        if b is entry:
            continue
        strict = set(dom[b]) - {b}
        if not strict:
            continue
        idom = max(strict, key=lambda x: len(dom[x]))
        children[idom].append(b)

    return children


def compute_postdom_children(blocks):
    """Compute post-dominator tree children using reversed CFG."""
    if not blocks:
        return {}

    exit_blocks = [b for b in blocks if not b.succs]
    if not exit_blocks:
        return {b: [] for b in blocks}

    # Sentinel for virtual exit
    VIRTUAL_EXIT = "___virtual_exit___"
    all_nodes = list(blocks) + [VIRTUAL_EXIT]
    all_set = set(all_nodes)

    # Reversed CFG predecessors.
    # Original edge A→B becomes reversed edge B→A.
    # Predecessors of X in reversed CFG = successors of X in original CFG.
    # Additionally, virtual_exit has edges TO each exit block, so exit blocks
    # have virtual_exit as a predecessor in the reversed CFG.
    exit_set = set(exit_blocks)

    def rev_preds(node):
        if node is VIRTUAL_EXIT:
            return []  # root of reversed CFG has no predecessors
        result = list(node.succs)  # successors in original = predecessors in reversed
        if node in exit_set:
            result.append(VIRTUAL_EXIT)
        return result

    dom = {}
    for n in all_nodes:
        dom[n] = frozenset({n}) if n is VIRTUAL_EXIT else frozenset(all_set)

    changed = True
    while changed:
        changed = False
        for n in all_nodes:
            if n is VIRTUAL_EXIT:
                continue
            preds = rev_preds(n)
            if not preds:
                continue
            pred_doms = [dom[p] for p in preds if p in dom]
            if not pred_doms:
                continue
            new_dom = frozenset(set.intersection(*[set(d) for d in pred_doms]) | {n})
            if new_dom != dom[n]:
                dom[n] = new_dom
                changed = True

    # Build children (only real blocks)
    children = {b: [] for b in blocks}
    for b in blocks:
        strict = set(dom[b]) - {b}
        real_strict = [d for d in strict if d is not VIRTUAL_EXIT]
        if not real_strict:
            continue
        ipdom = max(real_strict, key=lambda x: len(dom[x]))
        children[ipdom].append(b)

    return children


