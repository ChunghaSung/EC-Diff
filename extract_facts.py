def load_match_file(filename):
    """Load a statementMatch.txt or lockMatch.txt file.

    Format per line: p1ID:p2ID
    Returns dict mapping p2ID -> p1ID (for remapping P2 IDs to P1 equivalents).
    """
    result = {}
    if not filename or not os.path.exists(filename):
        return result
    with open(filename) as f:
        for line in f:
            line = line.strip()
            if not line or ':' not in line:
                continue
            p1_id, p2_id = line.split(':', 1)
            p1_id = p1_id.strip()
            p2_id = p2_id.strip()
            if p1_id and p2_id:
                result[p2_id] = p1_id
    return result


