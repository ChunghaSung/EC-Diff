# LLVM IR parser
# ---------------------------------------------------------------------------

def parse_ll(filename):
    with open(filename) as f:
        lines = f.readlines()

    module = Module()
    i = 0
    while i < len(lines):
        line = lines[i]

        # Match function definition
        m = re.match(r'^define\s+.*?@([\w.]+)\s*\(', line)
        if m:
            func_name = m.group(1)
            func = Function(func_name)

            # Skip to opening brace (usually on the same line)
            while i < len(lines) and '{' not in lines[i]:
                i += 1
            i += 1  # move past the { line

            current_bb = BasicBlock("entry")

            while i < len(lines):
                bline = lines[i]
                stripped = bline.strip()

                if stripped == '}':
                    break

                if not stripped or stripped.startswith(';'):
                    i += 1
                    continue

                # Label: starts at column 0 (not indented) and contains ':'
                if bline[0:1] != ' ' and bline[0:1] != '\t' and ':' in bline:
                    label_text = bline.split(':')[0].strip()
                    # Verify it looks like a label (not a global def)
                    if re.match(r'^[\w.]+$', label_text):
                        if current_bb.instructions:
                            func.blocks.append(current_bb)
                            func.block_map[current_bb.label] = current_bb
                        current_bb = BasicBlock(label_text)
                        i += 1
                        continue

                inst = Instruction(stripped)
                current_bb.instructions.append(inst)
                i += 1

            # Save last block
            if current_bb.instructions:
                func.blocks.append(current_bb)
                func.block_map[current_bb.label] = current_bb

            func.build_cfg()
            module.functions.append(func)
            module.func_map[func_name] = func

        i += 1

    return module


