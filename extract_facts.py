# ---------------------------------------------------------------------------

def main():
    if len(sys.argv) < 3:
        print("Usage: python3 extract_facts.py <input.ll> <pNumber>"
              " [statementMatch.txt] [lockMatch.txt]")
        sys.exit(1)

    ll_file = sys.argv[1]
    p_number = int(sys.argv[2])

    if p_number not in (1, 2):
        print("pNumber must be 1 or 2")
        sys.exit(1)

    st_match_file = sys.argv[3] if len(sys.argv) > 3 else None
    lock_match_file = sys.argv[4] if len(sys.argv) > 4 else None

    st_num_map = {}
    lock_num_map = {}
    if p_number == 2:
        st_num_map = load_match_file(st_match_file)
        lock_num_map = load_match_file(lock_match_file)

    # Parse
    module = parse_ll(ll_file)

    # Extract facts
    ext = FactExtractor(module, p_number, st_num_map, lock_num_map)
    ext.find_threads()
    ext.find_locks()
    ext.generate_facts()

    # Write output
    output_path = ll_file + ".facts.smt2"
    with open(output_path, 'w') as f:
        f.write(ext.get_output())
    print(f"Generated {output_path}")

    # Debug dumps
    base = ll_file
    ext.dump_thread_map(base + ".threadMap.txt")
    ext.dump_inst_map(base + ".instMap.txt")
    ext.dump_lock_map(base + ".lockMap.txt")


if __name__ == "__main__":
    main()
