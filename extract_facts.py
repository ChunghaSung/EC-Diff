class FactExtractor:
    """Extracts Datalog ground facts from a parsed LLVM IR module.

    Replicates the behavior of the original C++ DiffDep LLVM pass,
    including ID assignment order and output format.
    """

    LOCK_CALL_TARGETS = frozenset([
        'pthread_mutex_lock', 'pthread_mutex_unlock',
        'pthread_join', 'pthread_cond_wait', 'pthread_cond_signal',
        'nop1',
    ])

    def __init__(self, module, p_number, st_num_map=None, lock_num_map=None):
        self.module = module
        self.p_number = p_number
        self.p_bit = "#b0" if p_number == 1 else "#b1"

        self.st_num_map = st_num_map or {}
        self.lock_num_map = lock_num_map or {}

        # Counters
        self.inst_counter = 0
        self.lock_inst_counter = 0
        self.thread_counter = 0
        self.lock_var_counter = 0
        self.shared_var_counter = 0

        # ID maps  —  keyed by (func_name, bb_label, inst_index)
        self.inst_id = {}
        self.lock_inst_id = {}

        # Thread bookkeeping
        self.thread_map = {}        # name -> hex_id
        self.thread_map_rev = {}    # hex_id -> name
        self.thread_tid_map = {}    # alloca ref (e.g. "%2") -> thread func name

        # Lock & shared variable maps
        self.lock_var_map = {}      # global name -> hex_id
        self.shared_var_map = {}    # global name -> hex_id

        # Per-function load maps for pthread_join tid tracing
        self.load_maps = {}         # func_name -> {result_var -> source_ptr}

        # Fact accumulation strings (order matches original output)
        self.f_thread_create = []
        self.f_thread_join = []
        self.f_thread_cond = []     # threadCondWait + threadCondSignal
        self.f_st = []
        self.f_impo = []
        self.f_lock = []
        self.f_nolock = []
        self.f_read = []            # read + busyRead + busyWrite
        self.f_write = []
        self.f_dom = []
        self.f_postdom = []

    # -- ID assignment (on-demand, matching original traversal order) -------

    def _get_id(self, func_name, bb_label, idx):
        key = (func_name, bb_label, idx)
        if key in self.lock_inst_id:
            return self.lock_inst_id[key]
        if key in self.inst_id:
            return self.inst_id[key]
        new_id = hex_id(self.inst_counter)
        self.inst_counter += 1
        if self.p_number == 2 and new_id in self.st_num_map:
            new_id = self.st_num_map[new_id]
        self.inst_id[key] = new_id
        return new_id

    def _register_lock_inst(self, func_name, bb_label, idx):
        key = (func_name, bb_label, idx)
        new_id = hex_id_lock(self.lock_inst_counter)
        self.lock_inst_counter += 1
        if self.p_number == 2 and new_id in self.st_num_map:
            new_id = self.st_num_map[new_id]
        self.lock_inst_id[key] = new_id

    def _get_thread_id(self, name):
        if name in self.thread_map:
            return self.thread_map[name]
        tid = hex_id(self.thread_counter)
        self.thread_counter += 1
        self.thread_map[name] = tid
        self.thread_map_rev[tid] = name
        return tid

    def _get_lock_var_id(self, name):
        if name in self.lock_var_map:
            return self.lock_var_map[name]
        new_id = hex_id(self.lock_var_counter)
        self.lock_var_counter += 1
        if self.p_number == 2 and new_id in self.lock_num_map:
            new_id = self.lock_num_map[new_id]
        self.lock_var_map[name] = new_id
        return new_id

    def _get_shared_id(self, name):
        if name in self.shared_var_map:
            return self.shared_var_map[name]
        new_id = hex_id(self.shared_var_counter)
        self.shared_var_counter += 1
        self.shared_var_map[name] = new_id
        return new_id

    # -- Phase 1: find threads ----------------------------------------------

    def find_threads(self):
        self._get_thread_id("main")
        for func in self.module.functions:
            for bb in func.blocks:
                for inst in bb.instructions:
                    if not inst.is_call:
                        continue
                    target = inst.get_call_target()
                    if target != "pthread_create":
                        continue
                    # 3rd arg (index 2) = thread function
                    func_val = inst.get_call_arg_value(2)
                    gname = extract_global_name(func_val)
                    if gname:
                        self._get_thread_id(gname)
                        # 1st arg (index 0) = tid alloca pointer
                        tid_val = inst.get_call_arg_value(0)
                        if tid_val:
                            self.thread_tid_map[tid_val] = gname

    # -- Phase 2: find & pre-register lock instructions ---------------------

    def find_locks(self):
        for func in self.module.functions:
            for bb in func.blocks:
                for idx, inst in enumerate(bb.instructions):
                    if not inst.is_call:
                        continue
                    target = inst.get_call_target()
                    if target not in self.LOCK_CALL_TARGETS:
                        continue
                    self._register_lock_inst(func.name, bb.label, idx)
                    if target in ('pthread_mutex_lock', 'pthread_mutex_unlock'):
                        lock_val = inst.get_call_arg_value(0)
                        gname = extract_global_name(lock_val)
                        if gname:
                            self._get_lock_var_id(gname)

    # -- Phase 3: build load maps for join tracking -------------------------

    def _build_load_maps(self):
        for func in self.module.functions:
            lm = {}
            for bb in func.blocks:
                for inst in bb.instructions:
                    if inst.is_load and inst.result_var:
                        ptr = inst.get_load_pointer()
                        if ptr:
                            lm[inst.result_var] = ptr
            self.load_maps[func.name] = lm

    # -- Phase 4: main fact generation pass ---------------------------------

    def generate_facts(self):
        self._build_load_maps()

        for func in self.module.functions:
            if func.name == "nop1":
                continue
            if not func.blocks:
                continue

            thread_id = "#x0000"
            if func.name in self.thread_map:
                thread_id = self.thread_map[func.name]

            # Process each basic block's instructions
            for bb in func.blocks:
                self._process_bb(func, bb, thread_id)

            # Inter-BB program order
            for bb in func.blocks:
                self._inter_bb_po(func, bb)

            # Dominator / Post-dominator inter-BB facts
            self._dom_postdom(func)

    def _process_bb(self, func, bb, thread_id):
        p = self.p_bit
        for idx, inst in enumerate(bb.instructions):
            iid = self._get_id(func.name, bb.label, idx)

            # st fact
            self.f_st.append(f"(rule (st {iid} {thread_id} {p}))")

            # Intra-BB program order + dom + postDom
            if idx + 1 < len(bb.instructions):
                nid = self._get_id(func.name, bb.label, idx + 1)
                self.f_impo.append(f"(rule (impo {iid} {nid} {p}))")
                self.f_dom.append(f"(rule (dom {iid} {nid} {p}))")
                self.f_postdom.append(f"(rule (postDom {nid} {iid} {p}))")

            # Categorize
            if inst.is_call:
                self._handle_call(func, inst, iid, thread_id)
            elif inst.is_store:
                self._handle_store(func, inst, iid)
            elif inst.is_load:
                self._handle_load(func, inst, iid)
            else:
                self.f_nolock.append(f"(rule (nonLock {iid} {p}))")

    def _handle_call(self, func, inst, iid, thread_id):
        p = self.p_bit
        target = inst.get_call_target()

        if target == 'pthread_mutex_lock':
            gname = extract_global_name(inst.get_call_arg_value(0))
            if gname and gname in self.lock_var_map:
                lid = self.lock_var_map[gname]
                self.f_lock.append(f"(rule (lockAcquire {iid} {lid} {p}))")
            # No nonLock for lock/unlock

        elif target == 'pthread_mutex_unlock':
            gname = extract_global_name(inst.get_call_arg_value(0))
            if gname and gname in self.lock_var_map:
                lid = self.lock_var_map[gname]
                self.f_lock.append(f"(rule (lockRelease {iid} {lid} {p}))")

        elif target == 'pthread_create':
            gname = extract_global_name(inst.get_call_arg_value(2))
            if gname:
                tid = self._get_thread_id(gname)
                self.f_thread_create.append(
                    f"(rule (threadCreate {tid} {iid} {thread_id} {p}))")
            self.f_nolock.append(f"(rule (nonLock {iid} {p}))")

        elif target == 'pthread_join':
            join_val = inst.get_call_arg_value(0)
            lm = self.load_maps.get(func.name, {})
            if join_val and join_val in lm:
                tid_ptr = lm[join_val]
                if tid_ptr in self.thread_tid_map:
                    tname = self.thread_tid_map[tid_ptr]
                    tid = self._get_thread_id(tname)
                    self.f_thread_join.append(
                        f"(rule (threadJoin {tid} {iid} {thread_id} {p}))")
            self.f_nolock.append(f"(rule (nonLock {iid} {p}))")

        elif target == 'pthread_cond_wait':
            cond_name = extract_global_name(inst.get_call_arg_value(0))
            lock_name = extract_global_name(inst.get_call_arg_value(1))
            if cond_name and lock_name and lock_name in self.lock_var_map:
                sid = self._get_shared_id(cond_name)
                lid = self.lock_var_map[lock_name]
                self.f_thread_cond.append(
                    f"(rule (threadCondWait {sid} {lid} {iid} {p}))")
            self.f_nolock.append(f"(rule (nonLock {iid} {p}))")

        elif target == 'pthread_cond_signal':
            cond_name = extract_global_name(inst.get_call_arg_value(0))
            if cond_name:
                sid = self._get_shared_id(cond_name)
                # Original code stores signal facts in the condWait buffer
                self.f_thread_cond.append(
                    f"(rule (threadCondSignal {sid} {iid} {p}))")
            self.f_nolock.append(f"(rule (nonLock {iid} {p}))")

        else:
            self.f_nolock.append(f"(rule (nonLock {iid} {p}))")

    def _handle_store(self, func, inst, iid):
        p = self.p_bit
        ptr = inst.get_store_pointer()
        gname = extract_global_name(ptr)
        if gname:
            if "SHARED" in gname:
                sid = self._get_shared_id(gname)
                self.f_write.append(f"(rule (write {iid} {sid} {p}))")
            if "BUSY" in gname and func.name in self.thread_map:
                sid = self._get_shared_id(gname)
                # Original code stores busyWrite in readFacts
                self.f_read.append(f"(rule (busyWrite {iid} {sid} {p}))")
        self.f_nolock.append(f"(rule (nonLock {iid} {p}))")

    def _handle_load(self, func, inst, iid):
        p = self.p_bit
        ptr = inst.get_load_pointer()
        gname = extract_global_name(ptr)
        if gname:
            if "SHARED" in gname:
                sid = self._get_shared_id(gname)
                self.f_read.append(f"(rule (read {iid} {sid} {p}))")
            if "BUSY" in gname and func.name in self.thread_map:
                sid = self._get_shared_id(gname)
                self.f_read.append(f"(rule (busyRead {iid} {sid} {p}))")
        self.f_nolock.append(f"(rule (nonLock {iid} {p}))")

    def _inter_bb_po(self, func, bb):
        if not bb.instructions:
            return
        p = self.p_bit
        term_id = self._get_id(func.name, bb.label, len(bb.instructions) - 1)
        for succ in bb.succs:
            if succ.instructions:
                sid = self._get_id(func.name, succ.label, 0)
                self.f_impo.append(f"(rule (impo {term_id} {sid} {p}))")

    def _dom_postdom(self, func):
        if not func.blocks:
            return
        p = self.p_bit
        entry = func.blocks[0]

        dom_ch = compute_dom_children(func.blocks, entry)
        pdom_ch = compute_postdom_children(func.blocks)

        for b in func.blocks:
            for c in dom_ch.get(b, []):
                tid = self._get_id(func.name, b.label, len(b.instructions) - 1)
                fid = self._get_id(func.name, c.label, 0)
                self.f_dom.append(f"(rule (dom {tid} {fid} {p}))")

        for b in func.blocks:
            for c in pdom_ch.get(b, []):
                fid = self._get_id(func.name, b.label, 0)
                tid = self._get_id(func.name, c.label, len(c.instructions) - 1)
                self.f_postdom.append(f"(rule (postDom {fid} {tid} {p}))")

    # -- Output -------------------------------------------------------------

    def get_output(self):
        """Return all facts in the original output order."""
        sections = [
            self.f_thread_create,
            self.f_thread_join,
            self.f_thread_cond,
            self.f_st,
            self.f_impo,
            self.f_lock,
            self.f_nolock,
            self.f_read,
            self.f_write,
            self.f_dom,
            self.f_postdom,
        ]
        lines = []
        for section in sections:
            lines.extend(section)
        return "\n".join(lines) + "\n" if lines else ""

    def dump_thread_map(self, filename):
        with open(filename, 'w') as f:
            for name, tid in self.thread_map.items():
                f.write(f"{name}:{tid}\n")

    def dump_inst_map(self, filename):
        with open(filename, 'w') as f:
            for key, iid in sorted(self.inst_id.items(), key=lambda x: x[1]):
                f.write(f"{iid}:{key[0]}:{key[1]}:inst{key[2]}\n")
            for key, iid in sorted(self.lock_inst_id.items(), key=lambda x: x[1]):
                f.write(f"{iid}:{key[0]}:{key[1]}:inst{key[2]}\n")

    def dump_lock_map(self, filename):
        with open(filename, 'w') as f:
            for name, lid in self.lock_var_map.items():
                f.write(f"{name}:{lid}\n")


